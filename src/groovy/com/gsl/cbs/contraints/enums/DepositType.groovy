package com.gsl.cbs.contraints.enums

public enum DepositType {
    CASH("Cash"),
    CHEQUE("Cheque"),
    CREDIT_CARD("Card"),
    BANK_TRANSFER("Transfer")

    final String value

    DepositType(String value) {
        this.value = value
    }

    String toString() { value }

    String getKey() { name() }
}
