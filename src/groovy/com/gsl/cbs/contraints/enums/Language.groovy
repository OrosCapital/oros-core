public enum Language {
    ENGLISH("English"),
    BANGLA("Bangla"),
    JAPANS("JAPANS"),
    SPANISH("Spanish")

    final String value

    Language(String value) {
        this.value = value
    }

    String toString() { value }

    String getKey() { name() }
}